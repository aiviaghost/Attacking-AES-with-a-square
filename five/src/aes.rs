    fn test_mix_columns() {
        let res = AES128::state_to_block(AES128::mix_columns(&AES128::block_to_state(decode_hex(
            "636b6776f201ab7b30d777c5fe7c6f2b",
        ))));
        let expected = decode_hex("6a6a5c452c6d3351b0d95d61279c215c");
        assert_eq!(res, expected)
    }

