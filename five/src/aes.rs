type Word = [u8; 4];
type State = [[u8; 4]; 4];
type RoundKey = Vec<u8>;

impl AES128 {
    pub fn new(key: &str) -> Self {
        Self {
            key: decode_hex(key).try_into().unwrap(),
        }
    }

    fn rot_word(w: &Word) -> Word {
        [w[1], w[2], w[3], w[0]]
    }

    fn inv_rot_word(w: &Word) -> Word {
        [w[3], w[0], w[1], w[2]]
    }

    fn sub_word(w: &Word) -> Word {
        [
            SBOX[w[0] as usize],
            SBOX[w[1] as usize],
            SBOX[w[2] as usize],
            SBOX[w[3] as usize],
        ]
    }

    fn inv_sub_word(w: &Word) -> Word {
        [
            INV_SBOX[w[0] as usize],
            INV_SBOX[w[1] as usize],
            INV_SBOX[w[2] as usize],
            INV_SBOX[w[3] as usize],
        ]
    }

    fn rcon(index: usize) -> Word {
        [RCON[index], 0, 0, 0]
    }

    fn xor_word(w1: &Word, w2: &Word) -> Word {
        [w1[0] ^ w2[0], w1[1] ^ w2[1], w1[2] ^ w2[2], w1[3] ^ w2[3]]
    }

    fn key_expansion(key: &Vec<u8>, num_rounds: usize) -> Vec<RoundKey> {
        let mut round_keys = vec![key.to_owned()];
        for round_number in 1..=num_rounds {
            let first_word: Word = round_keys.last().unwrap()[..4].try_into().unwrap();
            let last_word: Word = round_keys.last().unwrap()[12..].try_into().unwrap();
            let transformed = Self::sub_word(&Self::rot_word(&last_word));
            let next_word = Self::xor_word(
                &Self::xor_word(&first_word, &transformed),
                &Self::rcon(round_number),
            );
            let mut next_key = vec![next_word];
            for i in 1..4 {
                next_key.push(Self::xor_word(
                    next_key.last().unwrap(),
                    &round_keys.last().unwrap()[4 * i..4 * (i + 1)]
                        .try_into()
                        .unwrap(),
                ));
            }
            round_keys.push(next_key.iter().flatten().map(|b| *b).collect());
        }
        round_keys
    }

    fn sub_bytes(state: &State) -> State {
        state.map(|c| Self::sub_word(&c))
    }

    fn shift_rows(state: &State) -> State {
        [
            [state[0][0], state[1][1], state[2][2], state[3][3]],
            [state[1][0], state[2][1], state[3][2], state[0][3]],
            [state[2][0], state[3][1], state[0][2], state[1][3]],
            [state[3][0], state[0][1], state[1][2], state[2][3]],
        ]
    }

    fn mix_columns(state: &State) -> State {
        state.map(|a| {
            [
                MULT_BY_2[a[0] as usize] ^ MULT_BY_3[a[1] as usize] ^ a[2] ^ a[3],
                a[0] ^ MULT_BY_2[a[1] as usize] ^ MULT_BY_3[a[2] as usize] ^ a[3],
                a[0] ^ a[1] ^ MULT_BY_2[a[2] as usize] ^ MULT_BY_3[a[3] as usize],
                MULT_BY_3[a[0] as usize] ^ a[1] ^ a[2] ^ MULT_BY_2[a[3] as usize],
            ]
        })
    }

    fn add_round_key(state: &State, round_key: RoundKey) -> State {
        state
            .iter()
            .zip(Self::block_to_state(round_key).iter())
            .map(|(a, b)| Self::xor_word(a, b))
            .collect::<Vec<_>>()
            .try_into()
            .unwrap()
    }

    fn block_to_state(block: Vec<u8>) -> State {
        block
            .chunks(4)
            .map(|c| c.try_into().unwrap())
            .collect::<Vec<_>>()
            .try_into()
            .unwrap()
    }

    fn state_to_block(state: State) -> Vec<u8> {
        state.iter().flatten().map(|b| *b).collect()
    }
}

